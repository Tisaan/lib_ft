#include <stdio.h>

void	ft_memset(void *s, size_t n)
{
	unsigned char	*tmp;

	tmp = s;
	while (*tmp && ((tmp - (unsigned char *)s) <= n))
	{
		*tmp++ = 0;
	}
	return (s);
}
